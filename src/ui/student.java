    private boolean verificaNome() {
        return !(nome.split(" ").length < 2 && nome.split(" ")[0].length() < 5 &&
                nome.split(" ")[nome.split(" ").length - 1].length() < 5
        );
    }

    private boolean verificaData() {
        try {
            ddn = LocalDate.of(ano, mes, dia);
        } catch (DateTimeException e) {
            return false;
        }
        return true;
    }

    private boolean verificaAtributos(){
        return (verificaData() && verificaNome());
    }

    private void adicionarPasse(){
       passes.adicionarPasse(new EpassStudent(titular, escola, anoEscolar));
    }

    private boolean criarTitular() {
        nome = jTextField2.getText();
        email = jTextField3.getText();
        codigoDoc = jTextField4.getText();
        dia = Integer.parseInt(Box3.getSelectedItem().toString());
        mes = Integer.parseInt(Box1.getSelectedItem().toString());
        ano = Integer.parseInt(Box2.getSelectedItem().toString());
        escolha = Box5.getSelectedItem().toString();
        anoEscolar = Integer.parseInt(Box4.getSelectedItem().toString());
        if (verificaAtributos()) {
            switch (escolha) {
                case "BI" -> tipoDoc = TipoDocumento.BI;
                case "CUC" -> tipoDoc = TipoDocumento.CUC;
                case "PASSPORT" -> tipoDoc = TipoDocumento.PASSPORT;
                case "TER" -> tipoDoc = TipoDocumento.TER;
            }

            titular = new Titular(codigoDoc, tipoDoc, nome, ddn, email);
            return true;
        }
        return false;
    }

